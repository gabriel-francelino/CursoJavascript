const diaDaSemana = "segunda";

switch (diaDaSemana) {
  case "segunda":
    console.log("Hoje é segunda-feira.");
    break;
  case "terca":
    console.log("Hoje é terça-feira.");
    break;
  case "quarta":
    console.log("Hoje é quarta-feira.");
    break;
  default:
    console.log("Dia desconhecido!");
    break;
}
