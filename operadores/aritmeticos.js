let a = 10
let b = 5

console.log('Soma: ', (a + b)) // Soma: 15
console.log('Subtração: ', (a - b)) // Subtração: 5
console.log('Multiplicação: ', (a * b)) // Multiplicação: 50
console.log('Divisão: ', (a / b)) // Divisão: 2
console.log('Resto: ', (a % b)) // Resto: 0
console.log('Exponenciação: ', (a ** b)) // Exponenciação: 100000

// Operadores de incremento e decremento
console.log('Incremento: ', a++) // Incremento: 11
console.log('Decremento: ', b--) // Decremento: 4

// Operadores de atribuição
console.log('Atribuição: ', a = 10) // Atribuição: 10
console.log('Atribuição de soma: ', a += 10) // Atribuição de soma: 20
console.log('Atribuição de subtração: ', a -= 10) // Atribuição de subtração: 10
console.log('Atribuição de multiplicação: ', a *= 10) // Atribuição de multiplicação: 100
console.log('Atribuição de divisão: ', a /= 10) // Atribuição de divisão: 10
console.log('Atribuição de resto: ', a %= 10) // Atribuição de resto: 0
console.log('Atribuição de exponenciação: ', a **= 10) // Atribuição de exponenciação: 0


